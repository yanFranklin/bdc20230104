package cn.gtmap.realestate.common.core.domain;


import javax.persistence.Id;
import javax.persistence.Table;

@Table(name = "bdc_msg_main")
public class BdcMsgMain {
    @Id
    /**
     * 主键
     */
    private String mainid;
    /**
     * 发送方法
     */
    private String url;
    /**
     * configid
     */
    private String configid;
    /**
     * 发送节点
     */
    private String cute;
    /**
     * 查询参数sql
     */
    private String paramSql;
    /**
     * 查询条件
     */
    private String paramWhere;


    public String getMainid() {
        return mainid;
    }

    public void setMainid(String mainid) {
        this.mainid = mainid;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getConfigid() {
        return configid;
    }

    public void setConfigid(String configid) {
        this.configid = configid;
    }

    public String getCute() {
        return cute;
    }

    public void setCute(String cute) {
        this.cute = cute;
    }

    public String getParamSql() {
        return paramSql;
    }

    public void setParamSql(String paramSql) {
        this.paramSql = paramSql;
    }

    public String getParamWhere() {
        return paramWhere;
    }

    public void setParamWhere(String paramWhere) {
        this.paramWhere = paramWhere;
    }

    @Override
    public String toString() {
        return "BdcMsgMain{" +
                "mainid='" + mainid + '\'' +
                ", url='" + url + '\'' +
                ", configid='" + configid + '\'' +
                ", cute='" + cute + '\'' +
                ", paramSql='" + paramSql + '\'' +
                ", paramWhere='" + paramWhere + '\'' +
                '}';
    }
}
