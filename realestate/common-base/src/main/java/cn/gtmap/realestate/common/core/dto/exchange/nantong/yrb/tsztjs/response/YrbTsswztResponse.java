package cn.gtmap.realestate.common.core.dto.exchange.nantong.yrb.tsztjs.response;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * @author <a href="mailto:haungjian@gtmap.cn">huangjian</a>
 * @Date 2022/8/12
 * @description 1.16.    申报状态推送【C001】
 */
@XmlAccessorType(XmlAccessType.PROPERTY)
@XmlRootElement(name = "SBZTTSXXLIST")
public class YrbTsswztResponse {


    /**
     * fhm : 返回码
     * fhxx : 返回信息
     */

    private String fhm;
    private String fhxx;

    @XmlElement(name = "FHM")
    public String getFhm() {
        return fhm;
    }

    public void setFhm(String fhm) {
        this.fhm = fhm;
    }

    @XmlElement(name = "FHXX")
    public String getFhxx() {
        return fhxx;
    }

    public void setFhxx(String fhxx) {
        this.fhxx = fhxx;
    }

    @Override
    public String toString() {
        return "YrbTsswztResponse{" +
                "fhm='" + fhm + '\'' +
                ", fhxx='" + fhxx + '\'' +
                '}';
    }
}
