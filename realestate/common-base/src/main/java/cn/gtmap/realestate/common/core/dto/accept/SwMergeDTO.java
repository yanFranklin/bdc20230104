package cn.gtmap.realestate.common.core.dto.accept;

/**
 * @author <a href="mailto:caolu@gtmap.cn">caolu</a>
 * @version 1.0, 2022/9/20
 * @description 推送税务中的并案信息
 */
public class SwMergeDTO {
    /**
     * 并案 ID，并案业务标识，格式（A-B，同一并案业务 A 相同，B 递增。如 1-1、1-2、1-3）
     */
    private String mergeid;
    /**
     * 并案业务总数
     */
    private String mergezs;

    public String getMergeid() {
        return mergeid;
    }

    public void setMergeid(String mergeid) {
        this.mergeid = mergeid;
    }

    public String getMergezs() {
        return mergezs;
    }

    public void setMergezs(String mergezs) {
        this.mergezs = mergezs;
    }

    @Override
    public String toString() {
        return "SwMergeDTO{" +
                "mergeid='" + mergeid + '\'' +
                ", mergezs='" + mergezs + '\'' +
                '}';
    }
}
