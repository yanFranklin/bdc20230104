package cn.gtmap.realestate.common.core.dto.exchange.court;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * usermarker
 */
@XmlRootElement(name = "USERMARKER")
public class CourtKzUserDTO {
    CONDITION CONDITION;

    public CourtKzUserDTO.CONDITION getCONDITION() {
        return CONDITION;
    }

    public void setCONDITION(CourtKzUserDTO.CONDITION CONDITION) {
        this.CONDITION = CONDITION;
    }

    public static class CONDITION{
        String USERNAME;
        String PASSWORD;

        public String getUSERNAME() {
            return USERNAME;
        }

        public void setUSERNAME(String USERNAME) {
            this.USERNAME = USERNAME;
        }

        public String getPASSWORD() {
            return PASSWORD;
        }

        public void setPASSWORD(String PASSWORD) {
            this.PASSWORD = PASSWORD;
        }
    }
}
