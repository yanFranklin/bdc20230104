package cn.gtmap.realestate.common.core.dto.analysis;

/**
 * @author <a href="mailto:wangwei2@gtmap.cn">wangwei2</a>
 * @version 1.0, 2019/2/27
 * @description 接口查询查询DTO
 */
public class BdcInterfaceDTO {

    private int total;

    private Object data;

    public BdcInterfaceDTO() {
    }

    public BdcInterfaceDTO(int total, Object data) {
        this.total = total;
        this.data = data;
    }

    @Override
    public String toString() {
        return "BdcInterfaceDTO{" +
                "total=" + total +
                ", data=" + data +
                '}';
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }
}
