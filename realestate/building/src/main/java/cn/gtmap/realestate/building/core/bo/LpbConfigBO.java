package cn.gtmap.realestate.building.core.bo;

import java.util.List;

/**
 * @author <a href="mailto:liyinqiao@gtmap.cn">liyinqiao</a>
 * @version 1.0  2018-11-02
 * @description 楼盘表配置 基类
 */
public class LpbConfigBO {

    /**
     * 户室配置实体
     */
    private FwhsBO fwhs;

    /**
     * 外层链接
     */
    private List<LinkBO> links;

    private InfoBO info;

    public FwhsBO getFwhs() {
        return fwhs;
    }

    public void setFwhs(FwhsBO fwhs) {
        this.fwhs = fwhs;
    }

    public List<LinkBO> getLinks() {
        return links;
    }

    public void setLinks(List<LinkBO> links) {
        this.links = links;
    }

    public InfoBO getInfo() {
        return info;
    }

    public void setInfo(InfoBO info) {
        this.info = info;
    }
}
