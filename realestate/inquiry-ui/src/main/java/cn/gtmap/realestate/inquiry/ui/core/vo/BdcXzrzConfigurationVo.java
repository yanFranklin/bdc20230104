package cn.gtmap.realestate.inquiry.ui.core.vo;
/**
 * 配置文件下载日志封装类
 */
public class BdcXzrzConfigurationVo {
    /**
     * 服务器ip
     */
    private String host;
    /**
     * 服务器端口
     */
    private String port;
    /**
     * 服务器用户名
     */
    private String username;
    /**
     * 服务器密码
     */
    private String password;
    /**
     * 服务器路径
     */
    private String path;
    /**
     * 应用名称
     */
    private String applyName;
    private String name;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getApplyName() {
        return applyName;
    }

    public void setApplyName(String applyName) {
        this.applyName = applyName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BdcXzrzConfigurationVo(String host, String port, String username, String password, String path, String applyName, String name) {
        this.host = host;
        this.port = port;
        this.username = username;
        this.password = password;
        this.path = path;
        this.applyName = applyName;
        this.name = name;
    }
}
