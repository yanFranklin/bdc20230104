package cn.gtmap.realestate.etl.core.domian.wwsq;

import javax.persistence.Id;
import javax.persistence.Table;
import java.util.Date;

/**
 * @author <a href="mailto:liaoxiang@gtmap.cn">liaoxiang</a>
 * @version 1.0  2021/11/2
 * @description 外网申请创建记录表
 */
@Table(name = "bdc_wwsq_cjjl")
public class BdcWwsqCjjlDO {

    /**
     * 全局唯一,主键
     */
    @Id
    private String cjjlid;

    /**
     * 外网项目ID
     */
    private String wwxmid;

    /**
     * 创建成功登记工作流实例ID
     */
    private String gzlslid;

    /**
     * 创建失败原因
     */
    private String sbyy;

    /**
     * 外网受理编号
     */
    private String wwslbh;

    /**
     * 创建时间
     */
    private Date cjsj;

    /**
     * 更新时间
     */
    private Date gxsj;


    public String getCjjlid() {
        return cjjlid;
    }

    public void setCjjlid(String cjjlid) {
        this.cjjlid = cjjlid;
    }

    public String getWwxmid() {
        return wwxmid;
    }

    public void setWwxmid(String wwxmid) {
        this.wwxmid = wwxmid;
    }

    public String getGzlslid() {
        return gzlslid;
    }

    public void setGzlslid(String gzlslid) {
        this.gzlslid = gzlslid;
    }

    public String getSbyy() {
        return sbyy;
    }

    public void setSbyy(String sbyy) {
        this.sbyy = sbyy;
    }

    public String getWwslbh() {
        return wwslbh;
    }

    public void setWwslbh(String wwslbh) {
        this.wwslbh = wwslbh;
    }

    public Date getCjsj() {
        return cjsj;
    }

    public void setCjsj(Date cjsj) {
        this.cjsj = cjsj;
    }

    public Date getGxsj() {
        return gxsj;
    }

    public void setGxsj(Date gxsj) {
        this.gxsj = gxsj;
    }
}
