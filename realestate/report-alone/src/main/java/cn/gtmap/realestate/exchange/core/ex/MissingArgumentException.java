package cn.gtmap.realestate.exchange.core.ex;

/**
 * @author <a href="mailto:shenjian@gtmap.cn">shenjian</a>
 * @version 1.0, 2018/11/10
 * @description 缺失参数异常
 */
public class MissingArgumentException extends AppException{
    private static final long serialVersionUID = -7415901699994334093L;
    private static final String MISSING_ARG_MSG_PREFIX= "缺失参数";

    /**
     * @author <a href="mailto:shenjian@gtmap.cn">shenjian</a>
     * @param argName 参数名
     * @return
     * @description
     */
    public MissingArgumentException(String argName) {
        super(MISSING_ARG,MISSING_ARG_MSG_PREFIX+":"+argName);
    }
}
