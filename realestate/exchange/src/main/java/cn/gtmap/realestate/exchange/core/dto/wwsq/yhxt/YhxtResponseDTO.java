package cn.gtmap.realestate.exchange.core.dto.wwsq.yhxt;

/**
 * @author <a href="mailto:shaoliyao@gtmap.cn">shaoliyao</a>
 * @version 1.0  2020-01-16
 * @description 响应结果
 */
public class YhxtResponseDTO {

    private String msg;

    private String code;

    public YhxtResponseDTO() {
        this.msg = "成功";
        this.code = "200";
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
