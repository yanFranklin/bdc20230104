package cn.gtmap.realestate.exchange.core.dto.wwsq.hst;

import cn.gtmap.realestate.exchange.core.dto.wwsq.RequestHead;

public class RequestHstParam {
    private RequestHead head;

    private RequestHstData data;

    public RequestHead getHead() {
        return head;
    }

    public void setHead(RequestHead head) {
        this.head = head;
    }

    public RequestHstData getData() {
        return data;
    }

    public void setData(RequestHstData data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "RequestHstParam{" +
                "head=" + head +
                ", data=" + data +
                '}';
    }
}
