package cn.gtmap.realestate.exchange.core.dto.wwsq.init.ygdy;

import java.util.List;

public class InitYgdyRequestDTO {
    String appid;
    String time;
    String dwmc;
    List<InitYgdyData> content;
    List<ImageFj> imgs;

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDwmc() {
        return dwmc;
    }

    public void setDwmc(String dwmc) {
        this.dwmc = dwmc;
    }

    public List<InitYgdyData> getContent() {
        return content;
    }

    public void setContent(List<InitYgdyData> content) {
        this.content = content;
    }

    public List<ImageFj> getImgs() {
        return imgs;
    }

    public void setImgs(List<ImageFj> imgs) {
        this.imgs = imgs;
    }

    public static class ImageFj {
        //文件内容
        String SBase64;
        //文件名称
        String SCredname;
        //文件格式
        String SFileFormatType;

        public String getSBase64() {
            return SBase64;
        }

        public void setSBase64(String SBase64) {
            this.SBase64 = SBase64;
        }

        public String getSCredname() {
            return SCredname;
        }

        public void setSCredname(String SCredname) {
            this.SCredname = SCredname;
        }

        public String getSFileFormatType() {
            return SFileFormatType;
        }

        public void setSFileFormatType(String SFileFormatType) {
            this.SFileFormatType = SFileFormatType;
        }
    }
}
