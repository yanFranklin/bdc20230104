package cn.gtmap.realestate.exchange.core.dto.hefei.swxx.cx.response;
/*
 * @author <a href="mailto:huangzijian@gtmap.cn">huangzijian</a>
 * @version 1.0, 2018/11/28
 * @description  获取税务信息 请求头信息
 */
public class GetSwxxResponseHead {

    private String channel_id;//传入分配的称，例如马鞍山：AHMSBDC
    private String rtn_code;//状态码
    private String rtn_msg;//消息
    private String token;//各分配的密码，调取时候验证
    private String tran_date;//调取的时间，格式yyyymmdd
    private String tran_id;//调取的方法名称
    private String tran_seq;//32位uuid

    public String getChannel_id() {
        return channel_id;
    }

    public void setChannel_id(String channel_id) {
        this.channel_id = channel_id;
    }

    public String getRtn_code() {
        return rtn_code;
    }

    public void setRtn_code(String rtn_code) {
        this.rtn_code = rtn_code;
    }

    public String getRtn_msg() {
        return rtn_msg;
    }

    public void setRtn_msg(String rtn_msg) {
        this.rtn_msg = rtn_msg;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getTran_date() {
        return tran_date;
    }

    public void setTran_date(String tran_date) {
        this.tran_date = tran_date;
    }

    public String getTran_id() {
        return tran_id;
    }

    public void setTran_id(String tran_id) {
        this.tran_id = tran_id;
    }

    public String getTran_seq() {
        return tran_seq;
    }

    public void setTran_seq(String tran_seq) {
        this.tran_seq = tran_seq;
    }
}
