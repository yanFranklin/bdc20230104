package cn.gtmap.realestate.exchange.core.bo.reqProp;

/**
 * @author <a href="mailto:liyinqiao@gtmap.cn">liyinqiao</a>
 * @version 1.0  2019-05-24
 * @description
 */
public class BeanReqPropBO extends ReqProBO{

    private String className;

    private String beanName;

    private String methodName;

    private String params;

    private String paramVals;

    // 参数是否必填
    private boolean paramRequired = true;

    // 响应是否被处理为JSON 主要用于 分页响应
    private boolean responseJson;

    // 不需要传递参数的 方法
    private boolean noArgs;

    public String getBeanName() {
        return beanName;
    }

    public void setBeanName(String beanName) {
        this.beanName = beanName;
    }

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }

    public String getParams() {
        return params;
    }

    public void setParams(String params) {
        this.params = params;
    }

    public String getParamVals() {
        return paramVals;
    }

    public void setParamVals(String paramVals) {
        this.paramVals = paramVals;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public boolean isParamRequired() {
        return paramRequired;
    }

    public void setParamRequired(boolean paramRequired) {
        this.paramRequired = paramRequired;
    }

    public boolean isResponseJson() {
        return responseJson;
    }

    public void setResponseJson(boolean responseJson) {
        this.responseJson = responseJson;
    }

    public boolean isNoArgs() {
        return noArgs;
    }

    public void setNoArgs(boolean noArgs) {
        this.noArgs = noArgs;
    }
}
