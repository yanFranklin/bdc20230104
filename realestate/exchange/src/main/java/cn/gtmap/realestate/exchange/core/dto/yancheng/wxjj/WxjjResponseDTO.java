package cn.gtmap.realestate.exchange.core.dto.yancheng.wxjj;

import java.util.Date;

/**
 * @author <a href="mailto:zxy@gtmap.cn">zxy</a>
 * @date 2020/11/22 21:21
 */
public class WxjjResponseDTO {
    /**
     *缴费数量
     */
    private Integer paymentCount;
    /**
     * 房屋数量
     */
    private Integer hourseCount;
    /**
     * 缴费状态
     */
    private Integer sfzt;
    /**
     * 维修基金缴费人
     */
    private String wxjjjfr;
    /**
     * 缴费金额
     */
    private Double hj;
    /**
     * 缴费时间
     */
    private Date sfsj;

    public Integer getPaymentCount() {
        return paymentCount;
    }

    public void setPaymentCount(Integer paymentCount) {
        this.paymentCount = paymentCount;
    }

    public Integer getHourseCount() {
        return hourseCount;
    }

    public void setHourseCount(Integer hourseCount) {
        this.hourseCount = hourseCount;
    }


    public Integer getSfzt() {
        return sfzt;
    }

    public void setSfzt(Integer sfzt) {
        this.sfzt = sfzt;
    }

    public String getWxjjjfr() {
        return wxjjjfr;
    }

    public void setWxjjjfr(String wxjjjfr) {
        this.wxjjjfr = wxjjjfr;
    }

    public Double getHj() {
        return hj;
    }

    public void setHj(Double hj) {
        this.hj = hj;
    }

    public Date getSfsj() {
        return sfsj;
    }

    public void setSfsj(Date sfsj) {
        this.sfsj = sfsj;
    }

    @Override
    public String toString() {
        return "WxjjResponseDTO{" +
                "paymentCount=" + paymentCount +
                ", hourseCount=" + hourseCount +
                ", sfzt='" + sfzt + '\'' +
                ", wxjjjfr='" + wxjjjfr + '\'' +
                ", hj=" + hj +
                ", sfsj='" + sfsj + '\'' +
                '}';
    }
}
