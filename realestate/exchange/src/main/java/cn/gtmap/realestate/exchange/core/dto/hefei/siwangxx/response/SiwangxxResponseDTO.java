package cn.gtmap.realestate.exchange.core.dto.hefei.siwangxx.response;

import com.alibaba.fastjson.annotation.JSONField;

/**
 * @author <a href="mailto:liyinqiao@gtmap.cn">liyinqiao</a>
 * @version 1.0  2019-10-04
 * @description 死亡信息查询响应结构
 */
public class SiwangxxResponseDTO {

    /**
     * 死亡时间（注销时间）
     **/
    @JSONField(name = "SWRQ")
    private String SWRQ;

    @JSONField(name = "XM")
    private String XM;

    @JSONField(name = "GMSFHM")
    private String GMSFHM;

    public String getSWRQ() {
        return SWRQ;
    }

    public void setSWRQ(String SWRQ) {
        this.SWRQ = SWRQ;
    }

    public String getXM() {
        return XM;
    }

    public void setXM(String XM) {
        this.XM = XM;
    }

    public String getGMSFHM() {
        return GMSFHM;
    }

    public void setGMSFHM(String GMSFHM) {
        this.GMSFHM = GMSFHM;
    }
}
